                    <div class="blog-news-details blog-single-details">
                      <p>Este viernes, 22 de noviembre, la Fuerza Armada Nacional Bolivariana celebró el Día
Internacional del Músico en el Salón de Honor del Ministerio del Poder Popular para la Defensa,
escenario que se engalanó con la presencia de un selecto grupo de artistas militares y no militares
de prodigiosa trayectoria profesional en la creación y ejecución musical, baluartes de la cultura y
el sentir venezolano.</p>
                    <p>El acto fue presidido por el vicepresidente sectorial de Soberanía Política, Seguridad y Paz, y
ministro del Poder Popular para la Defensa, General en Jefe Vladimir Padrino López, en
compañía del Estado Mayor Superior de la Fuerza Armada Nacional Bolivariana (FANB).</p>
                    <p>Durante el desarrollo de esta actividad, la Banda Sinfónica “Comandante Supremo Hugo Rafael
Chávez Frías” interpretó los temas: “Venezuela”, “Tremenda Negra” y “La Pica Pica”, como
abreboca del talento que abraza la institución castrense.</p>
                    <p>De esta manera, en respuesta a la sublime expresión del talento venezolano, el GJ Padrino López
impuso la condecoración “Santa Cecilia” a trascendentes músicos que han demostrado su lealtad
a los principios bolivarianos durante décadas, entre ellos destacan: Gloria Martín Gómez, Carlos
Vicente Quintero, Julio César Delgado, Mónico Márquez, Edith Salcedo, Juan Carlos Núñez y
Justo Montenegro; además de importantes exponentes militares de esta forma del arte que
trasciende barreras culturales, apelando a la sensibilidad humana, cúspide que nos identifica
como especie.</p>
                    <p>Por esta razón, en reconocimiento a la ardua labor de las bandas y orquestas castrenses, el
ministro del Poder Popular para la Defensa entregó una importante dotación de instrumentos
musicales, accesorios y material diverso, con el objetivo de optimizar la operatividad de las
agrupaciones e impulsar el talento de nuestros soldados patriotas.
En este sentido, el General Padrino manifestó: “La música va conduciendo el quehacer militar, la
cotidianidad. Por eso, en cada cuartel tenemos a nuestros cuatristas, guitarristas, percusionistas
(…) porque la música trae energía y esperanza. Es sin duda, el lenguaje del alma”.</p>
                    <blockquote>
                        <p>Entre sus palabras, también expresó: “Nosotros, a cada agresión imperial le podemos contestar
con el arte, con un pasaje llanero, con gaitas, galerones, una canción de Alí Primera, porque
preferimos la paz antes que utilizar los medios para la guerra”, puntualizó.</p>
                    </blockquote>
                    <p>De la misma manera exaltó el sentimiento nacionalista y la gesta heroica de nuestros
libertadores, así como el legado fresco que nos dejó el Comandante Hugo Chávez. En este orden
de ideas, afirmó: “Nosotros hemos luchado por la paz y hemos salido victoriosos porque tenemos
ideas y convicciones claras. ¡La paz ha sido nuestra principal victoria!”.</p>
                    <p>Seguidamente se refirió a la situación que vive el continente latinoamericano, aseverando que los
pueblos ya no quieren modelos neoliberales, fascistas y racistas, que fomenten la división, la
destrucción y la muerte. En este sentido, hizo alusión al empleo de la religión como un arma de
penetración imperialista para impulsar la desestabilización, desvirtuando la verdadera esencia de
Jesucristo, por lo que profirió: “¡Hay que tener mucho cuidado con eso! (…) Que no se utilicen
falsamente los símbolos cristianos para derrocar gobiernos”.</p>
                    <p>Finalmente efectuó un llamado a la consciencia, al cese de la agresión contra los pueblos y a
respetar las constituciones y la ética de los Estados latinoamericanos</p> 

