                      <h2>GJ VLADIMIR PADRINO: “¡LA PAZ HA SIDO NUESTRA PRINCIPAL VICTORIA!”</h2>
                      <p>Por <a class="blog-author" href="#">MINSITERIO DE DEFENSA</a> <span class="blog-date">|28/12/20195</span></p>
