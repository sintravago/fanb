Comando Logístico del Ejército Bolivariano, direccionado por el G/D Carlos
Eduardo Gamboa García, forma parte del cumplimiento de los lineamientos
ordenados por el Comandante en Jefe de la FANB Nicolás Maduro Moros,
Presidente Constitucional de la República Bolivariana de Venezuela, quién a través
del G/J Vladimir Padrino López, Ministro del Poder Popular para la Defensa ordenó
los centros de producción en todas las Unidades de la FANB, dando así pasos
firmes y certeros a la auto sustentabilidad del Ejército Bolivariano.</p>
                    <p>El Comandante General del Ejército Bolivariano expresó su satisfacción por la
reinauguración de la torrefactora, “Estamos haciendo un Ejército Bolivariano
Independiente, estamos dando pasos importantes para doblegar la guerra
económica, para hacer un país inexpugnable”.</p>
                    <p>En el mismo sentido, adelantó que ya se han realizado acercamientos con otras
instituciones de la FANB “La producción de esta torrefactora satisface las
necesidades del Ejército Bolivariano y en un futuro inmediato, nuestra producción
de café y fororo, va a ser llevada a otras instituciones y sectores del país”.</p>
                   
                    <p><b>Servicio de alimentación produce 6.000 panes diarios</b><br/>
Durante la visita al Servicio de Alimentación, el Comandante General del Ejército
Bolivariano, visitó el área de producción panadera, donde pudo constatar el
trabajo que realizan para la producción de 6.000 panes diarios, que fortalecen y
garantizan la alimentación en las Unidades del Ejército Bolivariano.</p>

